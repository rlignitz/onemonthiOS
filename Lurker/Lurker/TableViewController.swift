import UIKit

class TableViewController: UITableViewController {
    
    var model: [Post] = []

    override func viewDidLoad() {
        super.viewDidLoad()

        // Uncomment the following line to preserve selection between presentations
        // self.clearsSelectionOnViewWillAppear = false

        // Uncomment the following line to display an Edit button in the navigation bar for this view controller.
        // self.navigationItem.rightBarButtonItem = self.editButtonItem()
        
        let url = NSURL(string: "https://www.reddit.com/.json")!
        
        let task = NSURLSession.sharedSession().dataTaskWithURL(url) { data, response, error in
            let json = JSON(data: data!)
            let array = json["data"]["children"].arrayValue
            for var object in array {
                let title = object["data"]["title"].string!
                let post = Post()
                post.title = title
                self.model.append(post)
            }
            
            dispatch_async(dispatch_get_main_queue()) {
                self.tableView.reloadData()
            }
            
        }
        task.resume()
    }

    override func didReceiveMemoryWarning() {
        super.didReceiveMemoryWarning()
        // Dispose of any resources that can be recreated.
    }

    // MARK: - Table view data source

    override func tableView(tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        return self.model.count
    }

    
    override func tableView(tableView: UITableView, cellForRowAtIndexPath indexPath: NSIndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCellWithIdentifier("MyCell", forIndexPath: indexPath) as! TableViewCell

        // Configure the cell...
        let post = self.model[indexPath.row]
        cell.titleLabel.text = post.title

        return cell
    }

